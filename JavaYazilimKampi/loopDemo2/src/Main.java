
public class Main {

	public static void main(String[] args) {
		int i = 1;
		
		do{
			System.out.println(i);
			i++;
		}
		while(i<10);
		
		System.out.println("End of while loop");
		
		System.out.println("-----------------------");
		
		int j = 2;
		
		do{
			System.out.println(j);
			j+=2;
		}
		while(j<10);
		
		System.out.println("End of while loop");
	}

}
