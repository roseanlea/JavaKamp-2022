
public class Main {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("End of loop");
		
		System.out.println("-----------------------");
		
		for (int i = 1; i <= 10; i+=2) {
			System.out.println(i);
		}
		System.out.println("End of loop");
		
	}

}
